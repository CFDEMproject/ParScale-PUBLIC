#ifndef PASC_COUPLING_BASE_H
#define PASC_COUPLING_BASE_H

namespace PASCAL_NS
{

class CouplingBase
{
    public:

      // pull
      virtual bool fill_container_from_coupling(class ContainerBase &container) const = 0;

      // push
      virtual bool dump_container_to_coupling(class ContainerBase &container) const = 0;

      // force a fill operation for all containers, e.g., for JSON-type coupling
      virtual bool forceFill() const {return false;};

};

} //end PASCAL_NS

#endif
